									            <p>In a 1000 story building, a common elevator that has physical buttons for each floor is not ideal. We didn’t consider this type of design for our building as it will end up covering all the walls by 1003 buttons, and it will also be harder to locate the floor numbers. To give you a heads up, we assume that it is a corporate and residential in one building. The first 500 floors are for the companies and the other 500 are for the residents. We plan to make it a smart elevator to make it convenient for the users. <br><br>
Since it is a thousand story building, it is expected that there will be at least 200 people in each floor then multiply by 1000, that’s a huge amount of people in a building. We plan to have 80 elevators, with the size of 2.5 X 2.5 meters for the door and 3.6 meters length for the walls inside. It can load up to 28-30 people with the total weight of 1815 kilograms and has a speed of 45mph, it can reach 95th floor in 43 seconds. Since it will take quite a long journey for the users to reach their desired floors, we want them to somehow, feel comfortable. That’s why we’re planning to put benches in the two sides of the elevator, but its priorities are the people with disabilities, pregnant woman and senior citizens. We’ll also put an automatic air freshener spray to maintain the fragrance, a CCTV for security, and a background music just like in the coffeeshops. <br><br>
The elevator has interactive screens and keycard tapping system in each wall. There will also be an LCD screen at the top-front to display the pressed floors and the current floor. The elevator also has a voice annunciator to dictate the current floor.<br><br>
For the security of the residents, we’re planning to use a keycard. The keycard will be unique for each resident, it will contain the floor number where their unit is located. Every time they will use the elevator, they’ll have to tap the card in the keycard tapping system to enter their floor and tap again to cancel. Since their units are located from 501-1000 floor, only the first 500 floors can be selected from the interactive screens, so in that case, non-residents have no access to their floors. It is also hassle-free for them because they don’t have to locate and press their floors in the interactive screen, rather they’ll just have to tap their card. We assume that other residents have no business in the other residential floors, that’s why we’ll make it unique so that only the residents and maintenance have the access in their own floors.<br><br>
For the employees, to access their floors, they will manually find and select their floors in the interactive screen. The interactive screen will display the first 100 floors, and to select the other floors, the user will just swipe the screen to the left. In each display, there will be an open, close, emergency, and go button below. Unlike in the usual elevators, we plan to include a “Go” button so that when all the users are done pressing their floors, the only time the elevator will start to operate is when the button is pressed. It is to avoid skipping user’s floor. After pressing the “Go” button, there will be an additional button in the screen to add and cancel floors. So that if there’s any case that a floor was accidentally pressed, they could still cancel it and if someone changes their mind to go to another floor, they can add floors as well.<br>
With this design, we wanted to make sure that users will feel safe, clean, relaxed, and to boost their energy before entering their floors.
